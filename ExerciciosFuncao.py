def soma (num1, num2):
    calcSoma = num1 + num2

    return calcSoma

print (soma(4,6))


def subtracao (num1, num2):
    calcSub = num1 - num2

    return calcSub

print (subtracao(10, 4))

def multiplicacao (num1, num2, num3):
    calcMulti = num1 * num2 * num3

    return calcMulti

print(multiplicacao(3,3,1))

def divisao (num1, num2):
    calcDivisao = num2 / num1

    return calcDivisao

print(divisao(2,10))

def dobro (num1, num2):
    calcDobro = num1 * 2, num2 * 2

    return calcDobro

print (dobro(10,20))

def metade (num1, num2):
    calcMetade = num1 / 2, num2 / 2

    return calcMetade

print(metade(50,20))